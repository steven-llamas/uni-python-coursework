my_string1 = "spring2022 cs220"
my_string2 = "spring2022 cs343"
print(my_string1)
print(my_string2)

# title() method displays each word in titlecase
print(my_string1.title())
print(my_string2.title())

# upper() method change a string to all uppercase
print(my_string1.upper())
print(my_string2.upper())

# lower() method change a string to all lowercase
print(my_string1.lower())
print(my_string2.lower())
