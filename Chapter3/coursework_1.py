squares = ["1", "4", "9", "16", "25"]  # squares list with 5 items
print("\nInitial list with five square values:")
print(squares)

print("\nSlicing a List:")
print(squares[0:3])  # print first 3 elements in a list, indices 0 through 3
print(squares[1:4])  # generate any subset of list, slice at index 1 and end at index 4

print(squares[:4])  # print the slice from beginning to index 4
print(squares[2:])  # print the slice from index 2 to end
print(squares[-2:])  # print the last two square values from the list
