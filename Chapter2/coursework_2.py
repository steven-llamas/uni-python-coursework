fruits = ["apple", "mango", "banana", "grapes", "peach"]
cars = ["toyota", "tesla", "hyundai", "audi", "porsche"]
squares = ["1", "4", "9", "16", "25"]

print(fruits)
print(cars)
print(squares)

print(fruits[3])  # To access fourth item, we request index 3
print(cars[0])  # First item of a list is at position 0
print(fruits[-1])  # Last item of list is at index -1
print(fruits[-2])  # Second item from end of list is at index -2
