cars = ["toyota", "tesla", "hyundai", "audi", "porsche"]
print("\n CARS LIST OF 5 CARS WE INITIALLY HAVE")
print(cars)

fruits = ["apple", "mango", "banana", "grapes", "peach"]
print("\n FRUITS LIST OF 5 FRUITS WE INITIALLY HAVE")
print(fruits)

squares = ["1", "4", "9", "16", "25"]
print("\n SQUARES LIST OF 5 SQUARES WE INITIALLY HAVE")
print(squares)

# Sorting cars list temporarily with sorted() method
print("\n 1. Sorted CARS list by sorted () method: ")
print(sorted(cars))
print(" 2. Original CARS list again: ")
print(cars)

# Sorting fruits list temporarily with sorted() method
print("\n 3. Sorted FRUITS list by sorted () method: ")
print(sorted(fruits))
print(" 4. Original FRUITS list again: ")
print(fruits)

# Sorting squares list temporarily with sorted() method
print("\n 5. Sorted SQUARES list by sorted () method: ")
print(sorted(squares))
print(" 6. Original SQUARES list again: ")
print(squares)
